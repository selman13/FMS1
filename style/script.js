// Asfalt hissesi
let asfalt_span_ac = document.getElementById('asfalt-ac');
let asfalt_span_bagla = document.getElementById('asfalt-bagla');
let asfalt_metn = document.getElementById('asfalt-metn');

asfalt_span_ac.addEventListener('click', () => {
    asfalt_metn.style.display = 'block';

    asfalt_span_ac.style.display = 'none';
})

asfalt_span_bagla.addEventListener('click', () => {
    asfalt_metn.style.display = 'none';

    asfalt_span_ac.style.display = 'inline-block';
})


